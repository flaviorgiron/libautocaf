    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jDateChooser1.getDate() != null) {
            Encoder e = Base64.getEncoder();
            Date d = jDateChooser1.getDate();
            d.setHours(23);
            d.setMinutes(59);
            d.setSeconds(59);
            Long l = d.getTime();
            jTextField1.setText(e.encodeToString(l.toString().getBytes()));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!jTextField2.getText().trim().equals("")) {
            Encoder en = Base64.getEncoder();
            Decoder de = Base64.getDecoder();

            Date d = new Date();
            d.setDate(d.getDate() + Integer.parseInt(jTextField2.getText()));
            d.setHours(23);
            d.setMinutes(59);
            d.setSeconds(59);

            Long l = d.getTime();
            jTextField3.setText(en.encodeToString(l.toString().getBytes()));

            String s1 = new String(de.decode(jTextField3.getText()));
            Long lo = Long.parseLong(s1);

            Date da = new Date(lo);
            jDateChooser2.setDate(da);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

